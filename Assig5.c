/* Write a program to display the ASCII value of \n,\r,\t,\a,\b */

#include<stdio.h>

int main()
{
    printf("\n The ASCII value of '\\n' is : %d",'\n');
    printf("\n The ASCII value of '\\t' is : %d",'\t');
    printf("\n The ASCII value of '\\r' is : %d",'\r');
    printf("\n The ASCII value of '\\a' is : %d",'\a');
    printf("\n The ASCII value of '\\b' is : %d\n",'\b');

    return 0;
}
